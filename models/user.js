// user.js
const Mongoose = require("mongoose");
const UserSchema = new Mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
});

const User = Mongoose.model("user", UserSchema)
module.exports = User
